import UIKit


extension UIFont {
    
    class func arialOf(size: CGFloat) -> UIFont! {
        
        return UIFont(name: "Arial", size: size)
    }
    
    class func futuraMediumOf(size: CGFloat) -> UIFont! {
        
        return UIFont(name: "Futura-Medium", size: size)
    }
}
